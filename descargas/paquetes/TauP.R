<h2>TauP.R: Earthquake Traveltime Calculations for 1-D Earth Models</h2>
<p>Evaluates traveltimes and ray paths using predefined Earth
        (or other planet) models.  Includes phase plotting routines.
        The IASP91 (Kennett and Engdahl, 1991 &lt;<a href="https://doi.org/10.1111%2Fj.1365-246X.1991.tb06724.x">doi:10.1111/j.1365-246X.1991.tb06724.x</a>&gt;)
	and AK135 (Kennett et al., 1995 &lt;<a href="https://doi.org/10.1111%2Fj.1365-246X.1995.tb03540.x">doi:10.1111/j.1365-246X.1995.tb03540.x</a>&gt;)
	Earth models are included, and most important arrival phases can be
	evaluated.</p>
